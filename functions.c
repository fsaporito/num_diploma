/* function.c */

#include <stdio.h>
#include <math.h>

#define  e = 2.7182818284

inline double function (double *param, int dimension, int functiontype, double x) {

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////
// Variables Definition
//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

	int i; // For Loop Variables
	double function; // Result


//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////
// Image Calculation
//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

	switch (functiontype) {

		case 1: { // Polynomial

			for (i=0; i<=dimension; i++) { 
			
				function += param[i]*(pow(x,i)); 
				
			}

			break;

		}

		case 2: { // a*e^x + b*x^n

			double a = param[0];
			double b = param[1];
			double n = param[2];

			function = a*(pow(e,x)) + b*(pow(x,n));

			break;

		}

		case 3: { // A*sin(mx+t) + B*cos(nx+p)

			double A = param[0];
			double m = param[1];
			double t = param[2];
			double B = param[3];
			double n = param[4];
			double p = param[5];

			function = A*(sin(m*x+t)) + B*(cos(n*x+p));

			break;

		}

	}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////
// Return
//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

	return function;

}
